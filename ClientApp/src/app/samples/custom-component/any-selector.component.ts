import { Component, forwardRef, Input } from "@angular/core";
import { NG_VALUE_ACCESSOR, ControlValueAccessor } from "@angular/forms";


@Component(
  {
    selector: 'any-selector',
    providers: [
      {
        provide: NG_VALUE_ACCESSOR,
        useExisting: forwardRef(() => AnySelectorComponent),
        multi: true
      }
    ],
    template: `
      <select [name]="name" [(ngModel)]="value">
          <option *ngFor="let state of stateList" [value]="state.abbreviation"> 
            {{state.name}}
          </option>
      </select>
  `
  }
)
export class AnySelectorComponent implements ControlValueAccessor  {
  @Input()
  name: string;
  @Input('value')
  val: string;

  stateList: State[] = states;
  onChange: any = () => { };
  onTouched: any = () => { };

  get value() {
    return this.val;
  }

  set value(val) {
    this.val = val;
    this.onChange(val);
    this.onTouched();
  }

  registerOnChange(fn) {
    this.onChange = fn;
  }

  registerOnTouched(fn) {
    this.onTouched = fn;
  }

  writeValue(value) {
    if (value) {
      this.value = value;
    }
  }
}



export interface State {
  name: string;
  abbreviation: string;
}

